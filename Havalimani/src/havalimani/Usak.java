package havalimani;

public class Usak {
    public String ucak_tipi;
    public int yolcu_kapastesi;
    public int ekonomik_koltuk_sayisi;
    public int standart_koltuk_sayisi;
    public int business_koltuk_sayisi;
    public int Personel_sayisi;
    public Usak(String ucak_tipi, int yolcu_kapastesi, int ekonomik_koltuk_sayisi, int standart_koltuk_sayisi, int business_koltuk_sayisi, int Personel_sayisi){
        this.ucak_tipi = ucak_tipi;
        this.yolcu_kapastesi = yolcu_kapastesi;
        this.ekonomik_koltuk_sayisi = ekonomik_koltuk_sayisi;
        this.standart_koltuk_sayisi = standart_koltuk_sayisi;
        this.business_koltuk_sayisi = business_koltuk_sayisi;
        this.Personel_sayisi = Personel_sayisi;
    }
}
